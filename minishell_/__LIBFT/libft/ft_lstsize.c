#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*ptr;
	int		count;

	ptr = lst;
	count = 0;
	if (!lst)
		return (count);
	while (ptr)
	{
		count++;
		ptr = ptr->next;
	}
	return (count);
}
